def MAX(sets):
    return (max(sets))
      
sets = set([3, 15, 29, 18, 25, 36, 10, 65, 75])
print(MAX(sets))

def MIN(sets):
    return (min(sets))

sets = set([47, 172, 109, 1, 4, 15])
print(MIN(sets))